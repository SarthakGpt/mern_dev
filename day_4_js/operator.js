//arithmetic operator
let a=12;
let b=3;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a++);
console.log(b--);
//assignment operator
let y=4;
let x=y;
console.log(x+=y);
console.log(x-=y);
console.log(x*=y);
console.log(x/=y);
console.log(x%=y);
console.log(x**=y);
//comparison oper
let p=12;
let q=33;
console.log(p==q);
console.log(p===q);
console.log(p!=q);
console.log(p!==q);
console.log(p<q);
console.log(p>q);
console.log(p<=q);
console.log(p>=q);
//conditional operator
(p>q)?console.log("p is big"):console.log("q is big");
//logical operator
let n1=12,n2=33;
console.log((n1>n2)&&(n1==n2));
console.log((n1>n2)||(n1==n2));
console.log((!n1));
//bitwise operator
console.log(5&1);
console.log(5|1);
console.log(~5);
console.log(5^1);
console.log(5<<1);
console.log(5<<1);
console.log(5>>1);
